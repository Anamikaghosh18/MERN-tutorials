// for -> when you know how many times you want to loop
for (let i = 0; i < 5; i++) {
    console.log("Iteration: " + i);
}   

// while -> when you a base condition to check
let j = 0;  
while (j < 5) {
    console.log("While Iteration: " + j);
    j++;
}

// do while -> will execute at least once
let k = 0;      
do {
    console.log("Do While Iteration: " + k);
    k++;
}while (k < 5);         
    